/* ── Donate modal ── */
function openDonate() {
  document.getElementById('donateModal').classList.add('open');
  document.body.style.overflow = 'hidden';
}
function closeDonate() {
  document.getElementById('donateModal').classList.remove('open');
  document.body.style.overflow = '';
}
function closeDonateOutside(e) {
  if (e.target === document.getElementById('donateModal')) closeDonate();
}

/* ── Copy IBAN ── */
function copyIban(el) {
  const text = el.textContent.trim();
  navigator.clipboard.writeText(text).then(() => {
    const hint = el.nextElementSibling;
    const original = hint.textContent;
    hint.textContent = '✓';
    hint.style.color = '#16a34a';
    setTimeout(() => { hint.textContent = original; hint.style.color = ''; }, 2000);
  });
}

/* ── Init ── */
document.addEventListener('DOMContentLoaded', () => {
  document.querySelectorAll('.main-nav a').forEach(link => {
    link.addEventListener('click', () => {
      document.getElementById('mainNav').classList.remove('open');
    });
  });
  document.addEventListener('keydown', e => { if (e.key === 'Escape') closeDonate(); });

  const saved = localStorage.getItem('dmd_lang') || 'ka';
  setLang(saved);

  const sections = ['about','mission','drugs','world','contact'];
  const observer = new IntersectionObserver(entries => {
    entries.forEach(e => {
      if (e.isIntersecting) {
        const id = e.target.id;
        document.querySelectorAll('.main-nav a').forEach(a => {
          const active = a.getAttribute('href') === '#' + id;
          a.style.color = active ? 'var(--navy)' : '';
          a.style.fontWeight = active ? '600' : '';
        });
      }
    });
  }, { threshold: 0.4 });
  sections.forEach(id => { const el = document.getElementById(id); if (el) observer.observe(el); });
});
